set -e

echo ""
echo "--------------------------------------------------------"
echo "ENTER URL TO PLAYLIST OR SONG"
echo "--------------------------------------------------------"
echo ""

read -r URL

if [ -z "$URL" ]; then
  echo "------------------------------------------------------"
  echo "No URL entered"
  echo "------------------------------------------------------"
  echo "EXITING"
  exit 1
fi

echo ""
echo "--------------------------------------------------------"
echo "DOWNLOADING"
echo "--------------------------------------------------------"
echo ""

spotdl --output "./Music/{artist}/{album}/{title}.mp3" "$URL"

echo ""
echo "--------------------------------------------------------"
echo "DOWNLOAD COMPLETE"
echo "--------------------------------------------------------"
